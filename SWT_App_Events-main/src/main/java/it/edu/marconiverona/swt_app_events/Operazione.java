package it.edu.marconiverona.swt_app_events;

public class Operazione {

    public int somma(int a, int b) {
        return a + b;
    }
    
    public int differenza(int a, int b) {
        return a - b;
    }
    
    public int prodotto(int a, int b) {
        return a * b;
    }
    
    public float quoziente(float a, float b) {
        return a / b;
    }

    // costruttore 
    public Operazione() {

    }

}
